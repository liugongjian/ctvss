<template>
  <div class="device-list__container">
    <template v-if="!showRole && !showRealGroup">
      <component :is="realGroupInProtocol" />
    </template>
    <template v-else-if="showRole">
      <el-table v-show="deviceList.length" key="show-role-table" :data="deviceList" empty-text="暂无授权角色" fit class="device-list__table" @row-click="rowClick">
        <el-table-column label="角色ID" prop="deviceId" />
        <el-table-column label="角色名" prop="deviceName" />
        <el-table-column label="角色描述" prop="description" />
        <el-table-column label="角色授权账号" prop="userName" />
        <el-table-column prop="createdTime" label="创建时间" align="center" />
      </el-table>
    </template>
    <template v-else-if="showRealGroup">
      <el-table v-show="deviceList.length" key="show-real-group-table" :data="deviceList" empty-text="暂无业务组" fit class="device-list__table" @row-click="rowClick">
        <el-table-column label="业务组ID/名称">
          <template slot-scope="{row}">
            <div class="group-name">
              <div class="group-name__id">{{ row.deviceId }}</div>
              <div>
                {{ row.deviceName }}
              </div>
            </div>
          </template>
        </el-table-column>
        <el-table-column label="空间状态" align="center">
          <template slot-scope="{row}">
            <status-badge :status="row.deviceEnabled ? 'on' : 'off'" />
            {{ groupStatus[row.deviceEnabled ? 'on' : 'off'] }}
          </template>
        </el-table-column>
        <el-table-column label="接入类型" align="center">
          <template slot-scope="{row}">
            {{ inProtocolType[row.inProtocol] }}
          </template>
        </el-table-column>
        <el-table-column prop="devicePosition" label="接入区域" align="center" />
        <el-table-column prop="createdTime" label="创建时间" align="center" />
      </el-table>
    </template>
  </div>
</template>
<script lang="ts">
import { Component, Mixins } from 'vue-property-decorator'
import { GroupStatus, InProtocolType } from '@/dics'
import StatusBadge from '@/components/StatusBadge/index.vue'
import listMixin from '../mixin/listMixin'
import excelMixin from '../mixin/excelMixin'
import gb28181 from '../device-gb28181/list.vue'
import rtmp from '../device-rtmp/list.vue'
import rtsp from '../device-rtsp/list.vue'
import onvif from '../device-onvif/list.vue'
import ehome from '../device-ehome/list.vue'

@Component({
  name: 'DeviceVGroupList',
  components: {
    StatusBadge,
    gb28181,
    rtmp,
    rtsp,
    onvif,
    ehome
  }
})
export default class extends Mixins(listMixin, excelMixin) {
  private exportLoading = false
  private groupStatus = GroupStatus
  private inProtocolType = InProtocolType
  public get realGroupInProtocol() {
    return this.$route.query.realGroupInProtocol
  }
  /**
   * 导入设备表
   */
  private uploadExcel(data: any) {
    if (data.file.type === 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' || data.file.type === 'application/vnd.ms-excel') {
      this.dialog.uploadExcel = true
      this.selectedFile = data.file
      this.fileData = {
        groupId: this.groupId,
        inProtocol: this.inProtocol,
        dirId: this.dirId,
        fileName: data.file.name
      }
      if (this.isNVR) {
        this.fileData.parentDeviceId = this.deviceInfo.deviceId
        delete this.fileData.dirId
      }
    } else {
      this.$message.error('导入文件必须为表格')
    }
  }

  /**
   * 导出设备表
   */
  private async exportExcel(command: any) {
    this.exportLoading = true
    try {
      let params: any = {
        groupId: this.groupId,
        inProtocol: this.inProtocol,
        dirId: this.dirId,
        parentDeviceId: this.deviceId
      }
      // this.isNVR && (params.parentDeviceId = this.deviceInfo.parentDeviceId)
      if (command === 'exportAll') {
        params.command = 'all'
      } else {
        params.command = 'selected'
        let deviceArr: any = []
        if (command === 'exportCurrentPage') {
          deviceArr = this.deviceList
        } else if (command === 'exportSelect') {
          deviceArr = this.selectedDeviceList
        }
        params.deviceIds = deviceArr.map((device: any) => {
          return { deviceId: device.deviceId }
        })
      }
      await this.exportDevicesExcel(params)
    } catch (e) {
      this.$message.error('导出失败')
      console.log(e)
    }
    this.exportLoading = false
  }

  /**
   * 导出模板
   */
  private exportTemplate() {
    this.exelType = 'template'
    this.exelDeviceType = 'gb28181'
    this.exelName = 'GB28181导入模板'
    this.regionName = this.groupData?.regionName || ''
    this.excelGroupDate = this.groupData
    if (this.isNVR) {
      this.exelDeviceType = 'nvr'
      this.exelName = 'NVR添加子设备导入模板'
      this.excelInProtocol = this.deviceInfo.inProtocol
      this.parentDeviceId = this.deviceInfo.deviceId
    }
    this.exportExel()
  }
}
</script>
